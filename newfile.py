import os


மாணவர்களின்_தரவு = [
    "அருண்:95",
    "பிரியா:88",
    "சந்தோஷ்:76",
    "கவிதா:92"
]


வெளியீட்டு_ஃபைல்_பாதை = "student_report_log.txt" 

print("ஆட்டோமேஷன் பணி தொடங்கியது...")

try:
    
    with open(வெளியீட்டு_ஃபைல்_பாதை, 'w') as file:
        file.write("மாணவர்கள் அறிக்கை:\n")
        file.write("---------------------\n")
        
        for entry in மாணவர்களின்_தரவு:
            
            பெயர், மதிப்பெண்_str = entry.split(':')
            மதிப்பெண் = int(மதிப்பெண்_str)
            
            
            if மதிப்பெண் >= 90:
                நிலை = "A கிரேடு - சிறந்தது"
            else:
                நிலை = "B கிரேடு"
                
            report_line = f"பெயர்: {பெயர்}, மதிப்பெண்: {மதிப்பெண்}, நிலை: {நிலை}\n"
            file.write(report_line)
            print(f"✅ அச்சிடப்பட்டது: {பெயர்}")
            
    print("\n✅ ஆட்டோமேஷன் பணி நிறைவடைந்தது.")
    print(f"அறிக்கை இங்கே சேமிக்கப்பட்டுள்ளது: {வெளியீட்டு_ஃபைல்_பாதை}")

except Exception as e:
    print(f"❌ பிழை ஏற்பட்டது. ஆட்டோமேஷன் தோல்வி: {e}")
